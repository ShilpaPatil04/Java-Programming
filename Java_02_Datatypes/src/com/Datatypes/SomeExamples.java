package com.Datatypes;

public class SomeExamples {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 20;
		System.out.println(a+b); //30
		
		double c = 1.3;
		System.out.println(a+c); //11.3
		
		byte b1 = 125;
		byte b2 = 125;
		System.out.println(b1+b2); //byte +byte = int
		
		boolean t = true;
		boolean f = false;
		//System.out.println(t+f); --> we can't add boolean values

	}

}
