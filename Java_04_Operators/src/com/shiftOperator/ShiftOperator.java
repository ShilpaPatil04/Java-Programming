package com.shiftOperator;

public class ShiftOperator {

	public static void main(String[] args) {
	
		//Right Shift operator
		int a =10;
		System.out.println(a>>2); //2
		
		int z = -8;
		System.out.println(z>>3);//-1
		
		int z1 = -8;
		System.out.println(z1>>2);//-2

		//Left Shift Operator
		int b =9;
		System.out.println((b<<2)); //36
		
		int b1 = -5;
		System.out.println(b1<<2 );//-20
	}

}
