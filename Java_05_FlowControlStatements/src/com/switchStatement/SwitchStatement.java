package com.switchStatement;

public class SwitchStatement {
//switch statement in java
	public static void main(String[] args) {
		
		int marks = -10;
		switch(marks) {
		case 1: System.out.println("This is one");
		       break;
		case 4: System.out.println("This is four");
	       break;
		case 7: System.out.println("This is seven");
	       break;
		case 10: System.out.println("This is ten");
	       break;   
		default: System.out.println("No a number");   
		}
		

	}

}
